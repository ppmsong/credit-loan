package isec.loan.entity;

public class Contacter {
	private String relateionType;
	private String relateion;
	private String name;
	private String mobile;
	
	public String getRelateionType() {
		return relateionType;
	}

	public void setRelateionType(String relateionType) {
		this.relateionType = relateionType;
	}

	public String getRelateion() {
		return relateion;
	}

	public void setRelateion(String relateion) {
		this.relateion = relateion;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

}
